//  Write a program to find minimum and maximum number as well as to add two integer numbers using methods of Integer.

class Problem18{
    public static void main(String[] args){
        int num1 = 10;
        int num2 = 20;

        System.out.println("Max :"+ Integer.max(num1,num2));

        System.out.println("Min :"+ Integer.min(num1,num2));

        System.out.println("Sum :"+ Integer.sum(num1,num2));

    }
}